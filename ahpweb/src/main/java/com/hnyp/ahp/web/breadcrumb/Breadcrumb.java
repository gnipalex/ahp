package com.hnyp.ahp.web.breadcrumb;

public class Breadcrumb {
    
    private String title;
    private String url;
    
    public String getTitle() {
        return title;
    }
    public Breadcrumb setTitle(String title) {
        this.title = title;
        return this;
    }
    public String getUrl() {
        return url;
    }
    public Breadcrumb setUrl(String url) {
        this.url = url;
        return this;
    }

}
